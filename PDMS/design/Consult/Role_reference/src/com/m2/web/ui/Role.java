package com.m2.web.ui;

public class Role {
	
	private int id;
	
	private String name;
	
	private String nameToLink;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNameToLink() {
		return nameToLink;
	}

	public void setNameToLink(String nameToLink) {
		this.nameToLink = nameToLink;
	}
	
	
	
	

}
